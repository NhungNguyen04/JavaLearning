
public class Main {
//	wrapper class = provides a wayto use primitive datatypes as reference datatypes
//					reference datatypes contain useful methods
//					can be used with collections (ex.ArrayList)
//					
//	primitive		wrapper
//	-----------------------
//	boolean			Boolean
//	char			Character
//	int				Integer
//	double			Double
//	
//	autoboxing = the automatic convversion that Java compiler makes between the primitive types and wrapper classes
//	unboxing = the reverse of autoboxing. Automatic conversion of wrapper class to primitive
	
	Boolean a = true;
	Character c = 'n';
	Integer i = 15;
	Double d = 3.13;
	String s = "hi";
}
